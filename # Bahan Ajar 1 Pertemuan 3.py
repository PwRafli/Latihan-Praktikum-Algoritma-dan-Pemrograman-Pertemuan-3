# Bahan Ajar 1 Pertemuan 3

# Buat variabel
a = 45
b = 10

# Penjumlahan
hasil = a + b
print('hasil penjumlan :',hasil)

# Pengurangan
hasil = a - b
print('hasil pengurangan :',hasil)

# perkalaian
hasil = a * b
print('hasil perkalian :',hasil)

# pangkat
hasil = a ** b
print('hasil pangkat :',hasil)

# pembagian
hasil = a / b
print('hasil penjumlahan :',hasil)

# pembagian kebawah
hasil = a // b
print('hasil penjumlahan :',hasil)

# sisa bagi
hasil = a % b
print('hasil sisa bagi :',hasil)

