# Operator >
print('Apakah benar 16 > 2?', 16 > 2)
print('Apakah benar 10 > 5?', 10 > 5)

# Operator <
print('Apakah benar 30 < 8?', 30 < 8)
print('Apakah benar 71 < 2?', 71 < 2)

# Operator >=
print('Apakah benar 55 >= 5?', 55 >= 5)
print('Apakah benar 19 >= 14?', 19 >= 14)

# Operator <=
print('Apakah benar 22 <= 6?', 22 <= 6)
print('Apakah benar 80 <= 31?', 80 <= 31)

# Operator ==
print('Apakah benar 404 == 432?', 404 == 432)
print('Apakah benar 51 == 32?', 51 == 32)

# Operator !=
print('Apakah benar 44 != 51?', 44 != 51)
print('Apakah benar 70 != 71?', 70 != 71)
