# Operator Perhitungan
print('Hasil penjumlahan:', a + b)
print('Hasil pengurangan:', a - b)
print('Hasil perkalian:', a * b)
print('Hasil pangkat:', a ** b)
print('Hasil pembagian:', a / b)
print('Hasil pembagian kebawah:', a // b)
print('Hasil sisa bagi:', a % b)

# Operator Perbandingan
print('Apakah a lebih besar dari b?', a > b)
print('Apakah a lebih kecil dari b?', a < b)
print('Apakah a sama dengan b?', a == b)
print('Apakah a tidak sama dengan b?', a != b)
print('Apakah a lebih besar atau sama dengan b?', a >= b)
print('Apakah a lebih kecil atau sama dengan b?', a <= b)

# Operator Logika
print('Logika AND:', a > 2 and b < 5)
print('Logika OR:', a > 2 or b > 5)
print('Logika NOT:', not (a > 2))