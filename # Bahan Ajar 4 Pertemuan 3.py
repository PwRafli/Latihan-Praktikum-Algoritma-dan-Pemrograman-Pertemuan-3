print('Operator AND')
print('Semua statement benar:', (23 > 13) and (12 > 3))
print('Salah satu statement salah:', (23 > 13) and (12 < 3))
print('Kedua statement salah:', (23 < 13) and (12 < 3))
print('----------------------')

print('Operator OR')
print('Semua statement benar:', (23 > 13) or (12 > 3))
print('Salah satu statement benar:', (23 > 13) or (12 < 3))
print('Kedua statement salah:', (23 < 13) or (12 < 3))
print('----------------------')

print('Operator NOT')
print('Negasi dari True:', not True)
print('Negasi dari False:', not False)
print('Negasi dari (23 > 13):', not (23 > 13))
print('Negasi dari (12 < 3):', not (12 < 3))
print('----------------------')